void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) // ".GetLength" определение размера стороки(0)/столбца(1)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            //Console.Write($"{image[i, j]} ");
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FullImage(int row, int col) // Закрашивание области. Создаем перемнные строки "row" и столбца "col"
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        // Рекурсия, фунция (метод) который вызывает сам себя
        FullImage(row - 1, col); // вверх
        FullImage(row, col - 1); // влево 
        FullImage(row + 1, col); // вниз
        FullImage(row, col + 1); // вправо
    }
}

